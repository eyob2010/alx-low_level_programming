#include "main.h"
/**
 *  *  *  *  *  mul - a function that multiplies two numbers
 *    *   *   *  @a and @b : Accept two integers to be multiplied
 *     *    * @a : accepts one integer
 *      *   * @b : accepts the second integer
 *      *      *     *     *     *     * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}
